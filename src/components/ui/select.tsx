// SimpleSelect: a styled native select wrapper matching the old custom Select API
export interface SimpleSelectOption {
  value: string
  label: string
}

interface SimpleSelectProps {
  label?: string
  error?: string
  hint?: string
  required?: boolean
  options: SimpleSelectOption[]
  value?: string
  onChange?: (value: string) => void
  placeholder?: string
  disabled?: boolean
  id?: string
  name?: string
  className?: string
}

export const SimpleSelect: React.FC<SimpleSelectProps> = ({
  label,
  error,
  hint,
  required,
  options,
  value,
  onChange,
  placeholder,
  disabled,
  id,
  name,
  className,
}) => {
  const inputId = id ?? label?.toLowerCase().replace(/\s+/g, '-')

  return (
    <div className="flex flex-col gap-1.5">
      {label && (
        <label
          htmlFor={inputId}
          className="text-sm font-medium text-text-secondary"
        >
          {label}
          {required && <span className="text-danger ml-1">*</span>}
        </label>
      )}
      <div className="relative">
        <select
          id={inputId}
          name={name}
          value={value ?? ''}
          onChange={(e) => onChange?.(e.target.value)}
          disabled={disabled}
          className={cn(
            "h-9 w-full appearance-none rounded-lg border bg-card px-3 pr-9 text-sm text-text",
            "transition-all duration-150 outline-none",
            "focus:ring-2 focus:ring-primary focus:border-primary",
            error ? "border-danger focus:ring-danger focus:border-danger" : "border-border",
            !value && "text-text-muted",
            "disabled:opacity-50 disabled:cursor-not-allowed",
            className
          )}
        >
          {placeholder && (
            <option value="" disabled>
              {placeholder}
            </option>
          )}
          {options.map((opt) => (
            <option key={opt.value} value={opt.value} className="bg-card text-text">
              {opt.label}
            </option>
          ))}
        </select>
        <CaretDown
          className="pointer-events-none absolute right-3 top-1/2 -translate-y-1/2 text-text-muted"
          size={14}
        />
      </div>
      {error && <p className="text-xs text-danger">{error}</p>}
      {hint && !error && <p className="text-xs text-text-muted">{hint}</p>}
    </div>
  )
}
